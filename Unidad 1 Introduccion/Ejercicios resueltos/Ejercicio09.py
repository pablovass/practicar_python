'''
Mostrar por pantalla todos los numeros enteros entre 1 y 100, hacerlo con usando un
bucle while y tambien con un bucle for.
'''
a=1
while(a<101):
    print(a)
    a+=1
print('==========================')
for b in range(1,101):
    print(b)

print('==========================')
print(range(1,101))
